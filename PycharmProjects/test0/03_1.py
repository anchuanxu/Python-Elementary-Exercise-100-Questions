# 题目:一个整数,它加上100后是一个完全平方数,再加168又是一个完全平方数,请问该数字是多少?

## 程序分析:
# 1:假设该数字为X 存在条件X+100=n*n,X+100+168=m*m;得到等式(m-n)*(m+n)=168
# 2:进一步可以知道m-n=i m+n=j i*j=168,则知道i,j中至少存在一个偶数.
# 3:m=(i+j)/2,n=(i-j)/2,则可知道ij奇偶性一致 且i,j 大于2的偶数.j>=2,1<i<168/2+1
# 4:代码

for i in range(1,85):
    if 168 % i == 0:
        j = 168/i;
        if i > j and (i + j) % 2 == 0 and (i - j) % 2 == 0:
            m = (i + j) / 2
            n = (i - j) / 2
            x = n * n - 100
            print(x," 是符合条件的数字")